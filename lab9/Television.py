# Модуль Television.py
# Цей файл містить опис базового класу, який не залежить від інших.

class Television:
    """
    Базовий клас, що описує звичайний телевізор.
    Він містить основні характеристики: бренд, діагональ, ціну та стан живлення.
    """
    
    def __init__(self, brand, screen_size, price):
        """
        Конструктор класу. Викликається автоматично при створенні об'єкта.
        self - це посилання на конкретний об'єкт, який створюється прямо зараз.
        """
        # Ініціалізація атрибутів (полів) об'єкта
        self.brand = brand          # Зберігаємо назву бренду
        self.screen_size = screen_size  # Зберігаємо розмір екрану
        self.price = price          # Зберігаємо ціну
        self.is_on = False          # Початковий стан - вимкнений (False)

    def turn_on(self):
        """Метод для ввімкнення телевізора."""
        self.is_on = True  # Змінюємо стан перемикача
        print(f"Телевізор {self.brand} увімкнено.")

    def turn_off(self):
        """Метод для вимкнення телевізора."""
        self.is_on = False # Змінюємо стан перемикача
        print(f"Телевізор {self.brand} вимкнено.")

    def get_info(self):
        """
        Метод повертає рядок з інформацією про телевізор.
        Використовується для виведення даних у консоль.
        """
        # Тернарний оператор: обираємо слово залежно від значення self.is_on
        status = "Увімкнений" if self.is_on else "Вимкнений"
        
        # Повертаємо відформатований рядок (f-string)
        return f"ТБ: {self.brand}, Діагональ: {self.screen_size}\", Ціна: {self.price} грн, Стан: {status}"